function renderZombieWiki() {
  const container = document.getElementById('zombie-wiki-container');
  container.innerHTML = '';

  zombieWikiData.forEach(zombie => {
    const card = document.createElement('div');
    card.className = 'zombie-wiki-card';

    const body = document.createElement('div');
    body.className = 'zombie-wiki-body';

    body.innerHTML = `
      <h3>${zombie.name}</h3>
      <p class="zombie-wiki-desc">${zombie.description}</p>
      <div class="zombie-wiki-details">
        <p><strong>Alias:</strong> ${zombie.alias}</p>
        <p><strong>Type:</strong> ${zombie.type}</p>
        <p><strong>World:</strong> ${zombie.world}</p>
        <p><strong>HP:</strong> ${zombie.hitpoints}</p>
        <p><strong>Speed:</strong> ${zombie.speed}</p>
        <p><strong>DPS:</strong> ${zombie.dps}</p>
        <p><strong>Weight:</strong> ${zombie.weight}</p>
        <p><strong>Wave Cost:</strong> ${zombie.wave_cost}</p>
        <p><strong>Toughness:</strong> ${zombie.toughness ?? '-'}</p>
        <p><strong>Explodes When Mowed:</strong> ${zombie.explodes_when_mowed ? 'Yes' : 'No'}</p>
        <p><strong>Can Be Flicked:</strong> ${zombie.can_be_flicked ? 'Yes' : 'No'}</p>
      </div>
    `;

    card.appendChild(body);
    container.appendChild(card);
  });
}

document.addEventListener('DOMContentLoaded', renderZombieWiki);